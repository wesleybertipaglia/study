const sum = (a, b) => {
  return a + b;
};

const sub = (a, b) => {
  return a - b;
};

const mul = (a, b) => {
  return a * b;
};

const div = (a, b) => {
  return a / b;
};

const mol = (a, b) => {
  return a % b;
};

const exp = (a, b) => {
  return a ** b;
};

export { sum, sub, mul, div, mol, exp }