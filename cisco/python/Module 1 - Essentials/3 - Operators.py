# Operators

## Arithmetic operators
print(10 + 2)
print(10 - 2)
print(10 * 2)
print(10 / 2)
print(10 ** 2) # exponentiation
print(10 // 2) # floor division
print(11 % 2) # modulo

## Comparison operators
print(10 == 2)
print(10 != 2)
print(10 < 2)
print(10 <= 2)
print(10 >= 2)

## Logical operators
print(True and False)
print(True or False)
print(not True)
print(True & False)
print(True | False)
print(True ^ False)
print(~ False)
print(True | False)
print(True << False) # left shift
print(True >> False) # right shift

## Assignment operators
x = 10
x += 10  # Equivalent to x = x + 5
x -= 10
x *= 10
x /= 10
x //= 10
x %= 10
x **= 10

## Membership operators
is_in_list = 3 in [1, 2, 3]
is_not_in_list = 3 not in [1, 2, 3]

## Identity operators
a = [1, 2, 3]
b = a
is_same_object = a is b

## Strings operators
print("a" + "b")
print("a" * 2)