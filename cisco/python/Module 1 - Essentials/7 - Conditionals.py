# Conditionals

## If...
x = 10
if x > 5:
    print("x is greater than 5")

## If... else...
age = 20
if age >= 18:
    print("You are an adult")
else:
    print("You are a minor")

## Elif...
score = 85
if score >= 90:
    print("A")
elif score >= 80:
    print("B")
elif score >= 70:
    print("C")
else:
    print("Below C")