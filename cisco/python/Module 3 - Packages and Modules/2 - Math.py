import math

# Calculate the square root of a number
sqrt_result = math.sqrt(25)
print("Square root:", sqrt_result)

# Calculate the factorial of a number
factorial_result = math.factorial(5)
print("Factorial:", factorial_result)

# Calculate the value of pi
pi_value = math.pi
print("Pi:", pi_value)

# Calculate the value of e (Euler's number)
e_value = math.e
print("Euler's number:", e_value)

# Calculate the sine of an angle in radians
sine_result = math.sin(math.radians(30))  # Convert degrees to radians
print("Sine:", sine_result)

# Calculate the cosine of an angle in radians
cosine_result = math.cos(math.radians(60))  # Convert degrees to radians
print("Cosine:", cosine_result)

# Calculate the natural logarithm of a number
log_result = math.log(10)
print("Natural logarithm:", log_result)

# Calculate the base 10 logarithm of a number
log10_result = math.log10(100)
print("Base 10 logarithm:", log10_result)

# Calculate the exponential value of a number
exp_result = math.exp(2)
print("Exponential:", exp_result)

# Calculate the power of a number
power_result = math.pow(2, 3)
print("Power:", power_result)

# Calculate the absolute value of a number
abs_result = math.fabs(-5)
print("Absolute value:", abs_result)
